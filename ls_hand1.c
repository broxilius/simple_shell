#include "shell.h"

/**
 * add_sep_node_end -  Adds a new node with a separator to the end of sep_list.
 * @head: Pointer to the head of sep_list.
 * @sep: Separator character ; | &
 * Return: Pointer to the head of the updated sep_list.
 */
sep_list *add_sep_node_end(sep_list **head, char sep)
{
	sep_list *new, *temp;

	new = malloc(sizeof(sep_list));
	if (new == NULL)
		return (NULL);

	new->separator = sep;
	new->next = NULL;
	temp = *head;

	if (temp == NULL)
	{
		*head = new;
	}
	else
	{
		while (temp->next != NULL)
			temp = temp->next;
		temp->next = new;
	}

	return (*head);
}

/**
 * free_sep_list - Frees memory allocated for sep_list.
 * @head: Pointer to the head of sep_list.
 * Return: void
 */
void free_sep_list(sep_list **head)
{
	sep_list *temp;
	sep_list *curr;

	if (head != NULL)
	{
		curr = *head;
		while ((temp = curr) != NULL)
		{
			curr = curr->next;
			free(temp);
		}
		*head = NULL;
	}
}

